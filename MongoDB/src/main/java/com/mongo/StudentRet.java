package com.mongo;

public class StudentRet {



    private String name;
    private String grade;
    private String msg;

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }




    public void setName(String name) {
        this.name = name;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }


    public String getName() {
        return name;
    }
    public String getGrade() {
        return grade;
    }

}
