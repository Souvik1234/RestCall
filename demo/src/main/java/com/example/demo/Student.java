package com.example.demo;


public class Student {

	private String name;
	private String grade;


	public void setName(String name) {
		this.name = name;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getName() {
		return name;
	}
	public String getGrade() {
		return grade;
	}
	
	
}
